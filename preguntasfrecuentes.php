<h1>¿Qué es PlacetoPay? </h1>
<p class="lead">PlacetoPay es la plataforma de pagos electrónicos que usa tienda para procesar en línea las transacciones generadas en la tienda virtual con las formas de pago habilitadas para tal fin.</p>

<h1>¿Cómo puedo pagar? </h1>
<p class="lead">En la tienda virtual de tienda usted podrá realizar su pago con los medios habilitados para tal fin. Usted, de acuerdo a las opciones de pago escogidas por el comercio, podrá pagar a través de PSE (débitos desde cuentas de ahorros y corrientes en Colombia), tarjetas de crédito Visa, MasterCard, American Express, Credencial y Diners; Tarjeta Éxito, Tarjeta Alkosto, débitos internacionales a través de Safety Pay o acercándose a un punto de Western Union en cualquier lugar del mundo.
Además, si el comercio lo dispone así, podrá pagar en efectivo (Cajas de Almacenes Éxito, Carulla, Surtimax y Super Inter), además de PayPal e IVR PlacetoPay.
</p>

<h1>¿Es seguro ingresar mis datos bancarios en este sitio web?  </h1>
<p class="lead">Para proteger tus datos tienda delega en PlacetoPay la captura de la información sensible. Nuestra plataforma de pagos cumple con los más altos estándares exigidos por la norma internacional PCI DSS de seguridad en transacciones con tarjeta de crédito. Además tiene certificado de seguridad SSL expedido por GeoTrust una compañía Verisign, el cual garantiza comunicaciones seguras mediante la encriptación de todos los datos hacia y desde el sitio; de esta manera te podrás sentir seguro a la hora de ingresar la información de su tarjeta. 

Durante el proceso de pago, en el navegador se muestra el nombre de la organización autenticada, la autoridad que lo certifica y la barra de dirección cambia a color verde. Estas características son visibles de inmediato y dan garantía y confianza para completar la transacción en PlacetoPay.

